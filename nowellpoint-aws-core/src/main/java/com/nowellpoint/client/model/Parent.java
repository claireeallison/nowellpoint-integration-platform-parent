package com.nowellpoint.client.model;

public class Parent {
	
	private String type;
	
	private String id;
	
	private String href;
	
	public Parent() {
		
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getHref() {
		return href;
	}

	public void setHref(String href) {
		this.href = href;
	}
}