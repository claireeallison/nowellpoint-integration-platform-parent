package com.nowellpoint.aws.api;

public class Record {
	
	private String name;
	
	private String xmlType;
	
	public Record() {
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getXmlType() {
		return xmlType;
	}

	public void setXmlType(String xmlType) {
		this.xmlType = xmlType;
	}
}